#Comparing Between two numbers whose is greater number.

num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))


if num1 > num2: #comparing number
    print(f"The largest number is: {num1}")  #result
elif num1 < num2:
    print(f"The largest number is: {num2}") #result
else:
    print("Both numbers are equal.") #result